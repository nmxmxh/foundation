function toSnakeCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/([A-Z]+)([A-Z][a-z])/g, "$1_$2")
    .replace(/[^A-Za-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "")
    .toLowerCase();
}

function pluralizeSnake(value) {
  const parts = value.split("_");
  const last = parts.pop() ?? value;
  parts.push(pluralize(last));
  return parts.join("_");
}

function pluralize(value) {
  if (value.endsWith("s")) return value;
  if (value.endsWith("y") && !/[aeiou]y$/.test(value)) return `${value.slice(0, -1)}ies`;
  if (/(ch|sh|x|z)$/.test(value)) return `${value}es`;
  return `${value}s`;
}

function safePascalIdentifier(value) {
  const words = value
    .split(/[^A-Za-z0-9]+/)
    .flatMap((word) => word.split(/(?=[A-Z])/))
    .filter(Boolean);
  const identifier = words.map((word) => `${word[0].toUpperCase()}${word.slice(1)}`).join("");
  const safe = identifier.replace(/[^A-Za-z0-9_$]/g, "") || "Prototype";
  return /^[A-Za-z_$]/.test(safe) ? safe : `Prototype${safe}`;
}

function lowerFirst(value) {
  return `${value[0].toLowerCase()}${value.slice(1)}`;
}

function relativeToCwd(file) {
  return path.relative(process.cwd(), file) || ".";
}

